     */
    public function store(Request $request)
    {
        //
        $data = $request->validate([
          "name" => "required",
          "description" => "required",
          "amount" => "required",
          "days" => "required",
          "owner_id" => "required"
        ]);

        Campaign::create($data);
        return redirect(route('campaigns.index'));
    }


